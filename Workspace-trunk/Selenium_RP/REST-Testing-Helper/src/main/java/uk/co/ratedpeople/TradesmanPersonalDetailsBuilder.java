package uk.co.ratedpeople;


public class TradesmanPersonalDetailsBuilder {

	public static TradesmanPersonalDetails build(Integer salutationTypeId, String firstName, String lastName, String password, String confirmPassword, 
			Integer businessRoleTypeId) {
		TradesmanPersonalDetails obj = new TradesmanPersonalDetails();
		obj.setBusinessRoleTypeId(businessRoleTypeId);
		obj.setConfirmPassword(confirmPassword);
		obj.setFirstName(firstName);
		obj.setLastName(lastName);
		obj.setPassword(confirmPassword);
		obj.setSalutationTypeId(salutationTypeId);
		return obj;
	}
}

