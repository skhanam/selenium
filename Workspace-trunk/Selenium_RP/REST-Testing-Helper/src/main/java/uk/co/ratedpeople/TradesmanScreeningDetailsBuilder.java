package uk.co.ratedpeople;


public class TradesmanScreeningDetailsBuilder {

	public static TradesmanScreeningDetails build(String documentType, String ipAddress, Integer screeningRetryCount, Integer salutationTypeId, 
			String firstName, String middleName, String lastName, String dateOfBirth, Passport passport, 
			UKDrivingLicence ukDrivingLicence, DetailedAddress personalAddress) {
		TradesmanScreeningDetails obj = new TradesmanScreeningDetails();		
		obj.setDateOfBirth(dateOfBirth);
		obj.setDocumentType(documentType);
		obj.setFirstName(firstName);
		obj.setIpAddress(ipAddress);
		obj.setLastName(lastName);
		obj.setMiddleName(middleName);
		obj.setPassport(passport);
		obj.setPersonalAddress(personalAddress);
		obj.setSalutationTypeId(salutationTypeId);
		obj.setScreeningRetryCount(screeningRetryCount);
		obj.setUkDrivingLicence(ukDrivingLicence);
		return obj;
	}
}

