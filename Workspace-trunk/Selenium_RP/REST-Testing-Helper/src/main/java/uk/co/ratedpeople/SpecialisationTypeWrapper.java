package uk.co.ratedpeople;

import java.util.ArrayList;
import java.util.List;

public class SpecialisationTypeWrapper {

	private List<SpecialisationType> specialisationTypeList = new ArrayList<SpecialisationType>();
	
	public List<SpecialisationType> getSpecialisationTypeList() {
		return specialisationTypeList;
	}

	public void setSpecialisationTypeList(List<SpecialisationType> specialisationTypeList) {
		this.specialisationTypeList = specialisationTypeList;
	}
	
	public static class SpecialisationType{

		private Integer id;
		private String name;
		
		public Integer getId() {
			return id;
		}
		
		public void setId(Integer id) {
			this.id = id;
		}
		
		public String getName() {
			return name;
		}
		
		public void setName(String name) {
			this.name = name;
		}
	}
}
